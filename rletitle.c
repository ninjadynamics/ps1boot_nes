void wait(unsigned int f) {
  while(f-- > 0) {
    ppu_wait_nmi();
  }
}

void fade_in(unsigned int f) {
  byte vb = 0;
  while (vb++ < 4) {
    // set virtual bright value
    pal_bright(vb);
    wait(f);
  }
}

void fade_out(unsigned int f) {
  byte vb = 4;
  while (vb-- > 0) {
    // set virtual bright value
    pal_bright(vb);
    wait(f);
  }
}

void fade_to_white(unsigned int f) {
  byte vb = 4;
  while (vb++ < 8) {
    // set virtual bright value
    pal_bright(vb);
    wait(f);
  }
}

void fade_to_normal(unsigned int f) {
  byte vb = 8;
  while (vb-- > 4) {
    // set virtual bright value
    pal_bright(vb);
    wait(f);
  }
}

void fade_to_black(unsigned int f) {
  byte vb = 2;
  while (vb-- > 0) {
    // set virtual bright value
    pal_bright(vb);
    wait(f);
  }
}

void clear_screen() {
  vram_adr(0x2000);
  vram_fill(0x00, 1024);
}

void show_title_screen(const byte* pal, const byte* rle) {
  // disable rendering
  ppu_off();
  // set palette, virtual bright to 0 (total black)
  pal_bg(pal);
  //pal_bright(0);
  // unpack nametable into the VRAM
  vram_adr(0x2000);
  vram_unrle(rle);
  // enable rendering
  ppu_on_all();
  // fade in from black
  //fade_in();
}

